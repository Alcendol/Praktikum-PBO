package org.person;
public class Person {
    protected String name;
    protected int age;
    protected String address;
    protected int ID;

    public String getName(){
        return this.name;
    }
    public String getAddress(){
        return this.address;
    }
    public int getAge(){
        return this.age;
    }
    public int getID(){
        return this.ID;
    }
}
